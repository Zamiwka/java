
package src.operators;

public class Arithmetic {
    public static void main(String[] args) {

        int a3 = 1;
        int b3 = 3;
        System.out.println(a3 + b3); // 1 + 3
        System.out.println(a3 - b3); // 1 - 3
        System.out.println(a3 * b3); // 1 * 3
        System.out.println(a3 / b3); // 1 / 3

        int a1 = 11;
        int b1 = 2;
        System.out.println(a1 / b1);  // 11 / 2

/* operatory
 1- arifmeticheskie  + - * / % ++ --
 2- naznachenia      = += -= *= /= %= &= |= ^=
 3 - sravnenia       == != < > <= >=
 4- Logicheskie      && || !(net)
 */




        // primitive
        byte a = 12; // -128 127
        System.out.println(a);

        short b = 1000; // -32768  32767
        System.out.println(b);

        int c = 10000;
        int c1 = 10001;
        System.out.println(c);
        System.out.println(c1);


        long d = 1000000776868L;
        System.out.println(d);

        long d1 = 110;
        byte d2 = (byte) d1; // casting   convert
        System.out.println(d2);

        float e = 121245f; //32 byte
        System.out.println(e);

        double f = 2222656.56d; // 64 byte
        System.out.println(f);

        System.out.println("___");
        double f1 = 6.6;
        int f2 = (int) f1; // 6.6  6
        System.out.println(f2);

        System.out.println("____");

        char l = 'c';
        char m = 'C';
        char n = 78;
        String n1 = "s";

        System.out.println(l);
        System.out.println(m);
        System.out.println(n);

        boolean t = true;
        boolean p = false;
        System.out.println(t);
        System.out.println(p);

        // non-primitive (ссылочный)

        String slovo = "Privet";
        System.out.println(slovo);

        // массивы
        // interface
        // user defined



            }

        }




